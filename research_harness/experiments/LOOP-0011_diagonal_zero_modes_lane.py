#!/usr/bin/env python3
r"""LOOP-0011 diagonal-difference zero-mode classification lane.

Targets the four diagonal-difference Hessian zero modes left unclassified by
LOOP-0010's phase/product-unitary candidates.  The expanded local candidates are
active support-plane directions in the 2x2 atom plane supporting

    C0 = (|00><00| - |11><11|)/sqrt(2).

In matrix tensor notation (row/column index (i,a)), they are generated by
    A \otimes P   and   P \otimes B,
where P=|0><0|+|1><1| and A,B are off-diagonal matrix units in the active
{0,1} plane, plus the phase i C0.

This is a local numerical/symbolic diagnostic lane only, not a global proof.
"""
from __future__ import annotations

import argparse
import importlib.util
import json
import math
from pathlib import Path
from typing import Iterable

import numpy as np

ROOT = Path(__file__).resolve().parents[2]
LOOP8_PATH = ROOT / "research_harness/experiments/LOOP-0008_tangent_equality_lane.py"
LOOP10_PATH = ROOT / "research_harness/experiments/LOOP-0010_zero_mode_symbolic_classification.py"

spec8 = importlib.util.spec_from_file_location("loop8tan", LOOP8_PATH)
assert spec8 is not None and spec8.loader is not None
loop8 = importlib.util.module_from_spec(spec8)
spec8.loader.exec_module(loop8)

spec10 = importlib.util.spec_from_file_location("loop10zero", LOOP10_PATH)
assert spec10 is not None and spec10.loader is not None
loop10 = importlib.util.module_from_spec(spec10)
spec10.loader.exec_module(loop10)

D = 4
N = 16


def real_inner(X: np.ndarray, Y: np.ndarray) -> float:
    return float(np.real(loop8.hs(X, Y)))


def normalized(Dv: np.ndarray) -> np.ndarray:
    n = math.sqrt(loop8.fro2(Dv))
    return Dv / n if n else Dv


def active_matrix_unit(i: int, j: int) -> np.ndarray:
    M = np.zeros((D, D), dtype=np.complex128)
    M[i, j] = 1.0
    return M


def active_projector() -> np.ndarray:
    P = np.zeros((D, D), dtype=np.complex128)
    P[0, 0] = 1.0
    P[1, 1] = 1.0
    return P


def expanded_support_plane_directions(C0: np.ndarray) -> list[tuple[str, np.ndarray]]:
    """Candidate zero directions missing from product-unitary orbit span."""
    P = active_projector()
    E01 = active_matrix_unit(0, 1)
    E10 = active_matrix_unit(1, 0)
    dirs: list[tuple[str, np.ndarray]] = [("phase_iC0", 1j * C0)]
    for label, M in (("E01", E01), ("E10", E10)):
        K1 = np.kron(M, P)
        K2 = np.kron(P, M)
        dirs.append((f"first_active_plane_{label}_real", normalized(K1)))
        dirs.append((f"first_active_plane_{label}_imag", 1j * normalized(K1)))
        dirs.append((f"second_active_plane_{label}_real", normalized(K2)))
        dirs.append((f"second_active_plane_{label}_imag", 1j * normalized(K2)))
    return dirs


def candidate_matrix(dirs: Iterable[np.ndarray], C0: np.ndarray, rbasis: list[np.ndarray]) -> np.ndarray:
    cols = []
    for Dv in dirs:
        P = loop10.tangent_project(Dv, C0, rbasis)
        c = loop10.coords_in_rbasis(P, rbasis)
        if np.linalg.norm(c) > 1e-13:
            cols.append(c)
    if not cols:
        return np.zeros((len(rbasis), 0), dtype=np.float64)
    return np.column_stack(cols)


def support_signature(Dv: np.ndarray, tol: float = 1e-12) -> list[dict]:
    out = []
    for r, c in np.argwhere(np.abs(Dv) > tol):
        out.append(
            {
                "row": [int(r // D), int(r % D)],
                "col": [int(c // D), int(c % D)],
                "value_real": float(np.real(Dv[r, c])),
                "value_imag": float(np.imag(Dv[r, c])),
            }
        )
    return out


def one_parameter_checks(C0: np.ndarray, named_dirs: list[tuple[str, np.ndarray]]) -> dict:
    """Check that individual expanded rays stay rank<=2 and zero-gap at samples.

    This is intentionally only a one-parameter sanity check for each named ray;
    it does not prove that the full candidate span is a global equality manifold.
    """
    samples = [1e-3, 0.1, 1.0, -0.2]
    checks = {}
    for name, Dv in named_dirs:
        rows = []
        for t in samples:
            C = C0 + t * Dv
            rows.append(
                {
                    "t": float(t),
                    "rank_tol_1e-10": int(np.linalg.matrix_rank(C, tol=1e-10)),
                    "fro2": float(loop8.fro2(C)),
                    "gap": float(loop8.gap(C)),
                    "normalized_gap": float(loop8.normalized_gap(C)),
                }
            )
        checks[name] = rows
    return checks


def analyze(tol: float) -> dict:
    C0 = loop8.equality_examples()["diag_difference"]
    rbasis = loop8.orthonormalize_real_tangent_sphere(loop8.tangent_complex_basis(C0), C0)
    H = loop8.real_quadratic_matrix(rbasis)
    evals, evecs = np.linalg.eigh(H)
    zero_idx = np.where(np.abs(evals) <= tol)[0]
    zero_coords = evecs[:, zero_idx]

    phase_M = candidate_matrix([1j * C0], C0, rbasis)
    local_M = candidate_matrix(loop10.local_unitary_directions(C0), C0, rbasis)
    loop10_core_M = candidate_matrix(loop10.explicit_core_family_directions("diag_difference", C0), C0, rbasis)
    prior_M = np.column_stack([m for m in (phase_M, local_M, loop10_core_M) if m.shape[1]])

    named_expanded = expanded_support_plane_directions(C0)
    expanded_M = candidate_matrix([Dv for _, Dv in named_expanded], C0, rbasis)
    combined_M = np.column_stack([prior_M, expanded_M])

    Qprior, _ = loop10.real_orthonormal_columns(prior_M, tol)
    if Qprior.shape[1]:
        unclassified_coords = zero_coords - Qprior @ (Qprior.T @ zero_coords)
    else:
        unclassified_coords = zero_coords
    # Orthonormalize the residual columns to get a direct target basis for the four missing modes.
    target_Q, _ = loop10.real_orthonormal_columns(unclassified_coords, tol)

    reports = {
        "LOOP0010_prior_candidates": loop10.project_report(zero_coords, prior_M, tol),
        "expanded_support_plane_only": loop10.project_report(zero_coords, expanded_M, tol),
        "prior_plus_expanded_support_plane": loop10.project_report(zero_coords, combined_M, tol),
        "four_dim_prior_residual_target_vs_expanded": loop10.project_report(target_Q, expanded_M, tol),
    }

    # Rayleigh diagnostics for expanded orthonormal columns.
    Qexp, keep = loop10.real_orthonormal_columns(expanded_M, tol)
    rayleigh = [float(Qexp[:, j] @ H @ Qexp[:, j]) for j in range(Qexp.shape[1])]

    # Record a compact canonical support signature of all expanded directions.
    signatures = {name: support_signature(Dv) for name, Dv in named_expanded}

    return {
        "loop": "LOOP-0011",
        "claim": "CLAIM-0001-rank-two-partial-trace",
        "control": "diag_difference",
        "tol": tol,
        "method": (
            "Compared the 9-dimensional numerical Hessian zero eigenspace against "
            "LOOP-0010 prior candidates plus active 2x2 support-plane directions "
            "span{iC0, A⊗P, iA⊗P, P⊗B, iP⊗B for A,B=E01,E10}."
        ),
        "zero_count_tol": int(len(zero_idx)),
        "negative_count_tol": int(np.sum(evals < -tol)),
        "positive_count_tol": int(np.sum(evals > tol)),
        "eigenvalue_min": float(evals[0]),
        "eigenvalue_max": float(evals[-1]),
        "zero_eigenvalues": [float(evals[i]) for i in zero_idx],
        "candidate_reports": reports,
        "expanded_candidate_rank": int(Qexp.shape[1]),
        "expanded_candidate_kept_columns": [int(i) for i in keep],
        "expanded_candidate_rayleigh": {
            "min": float(min(rayleigh)) if rayleigh else None,
            "max": float(max(rayleigh)) if rayleigh else None,
            "abs_max": float(max(abs(x) for x in rayleigh)) if rayleigh else None,
            "values": rayleigh,
        },
        "expanded_direction_support_signatures": signatures,
        "one_parameter_rank_gap_checks": one_parameter_checks(C0, named_expanded),
        "classified_all_nine_zero_modes_locally": bool(reports["prior_plus_expanded_support_plane"]["contains_zero_space_tol"]),
        "classified_prior_four_residual_modes_locally": bool(reports["four_dim_prior_residual_target_vs_expanded"]["contains_zero_space_tol"]),
        "success_condition_met": False,
        "fail_closed_caveat": (
            "This is a local tangent-space classification of the numerical zero kernel. "
            "The sampled one-parameter zero-gap rays support the equality-family interpretation, "
            "but this is not a global proof of the inequality or a complete global equality classification."
        ),
    }



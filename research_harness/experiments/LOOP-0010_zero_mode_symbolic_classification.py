#!/usr/bin/env python3
"""LOOP-0010 zero-mode symbolic/equality-family classification lane.

Compares the numerical Hessian zero eigenspace from LOOP-0008/0009 with
analytic tangent directions generated by explicit equality-preserving families:
  * global phase i C0,
  * local left/right subsystem unitary orbit directions
    C -> (U1⊗U2) C (V1⊗V2)^*,
  * named equality-family directions (currently the same local product orbit,
    recorded separately from phase for auditability).

This is a local numerical containment test of tangent spaces, not a global proof.
"""
from __future__ import annotations

import argparse
import importlib.util
import json
import math
from pathlib import Path
from typing import Iterable

import numpy as np

ROOT = Path(__file__).resolve().parents[2]
LOOP8_PATH = ROOT / "research_harness/experiments/LOOP-0008_tangent_equality_lane.py"
spec = importlib.util.spec_from_file_location("loop8tan", LOOP8_PATH)
assert spec is not None and spec.loader is not None
loop8 = importlib.util.module_from_spec(spec)
spec.loader.exec_module(loop8)

D = 4
N = 16


def antihermitian_basis(d: int = D) -> list[np.ndarray]:
    """Real basis for u(d), anti-Hermitian matrices."""
    out: list[np.ndarray] = []
    for i in range(d):
        K = np.zeros((d, d), dtype=np.complex128)
        K[i, i] = 1j
        out.append(K)
    for i in range(d):
        for j in range(i + 1, d):
            K = np.zeros((d, d), dtype=np.complex128)
            K[i, j] = 1.0
            K[j, i] = -1.0
            out.append(K)
            L = np.zeros((d, d), dtype=np.complex128)
            L[i, j] = 1j
            L[j, i] = 1j
            out.append(L)
    return out


def real_inner(X: np.ndarray, Y: np.ndarray) -> float:
    return float(np.real(loop8.hs(X, Y)))


def tangent_project(Dv: np.ndarray, C0: np.ndarray, rbasis: list[np.ndarray]) -> np.ndarray:
    # Project to the real Frobenius unit-sphere tangent and then into the rank-2 tangent basis.
    Dv = Dv - real_inner(C0, Dv) * C0 / loop8.fro2(C0)
    out = np.zeros_like(C0)
    for e in rbasis:
        out += real_inner(e, Dv) * e
    return out


def coords_in_rbasis(Dv: np.ndarray, rbasis: list[np.ndarray]) -> np.ndarray:
    return np.array([real_inner(e, Dv) for e in rbasis], dtype=np.float64)


def real_orthonormal_columns(M: np.ndarray, tol: float) -> tuple[np.ndarray, list[int]]:
    Q_cols: list[np.ndarray] = []
    keep: list[int] = []
    for j in range(M.shape[1]):
        w = M[:, j].astype(np.float64).copy()
        for q in Q_cols:
            w -= np.dot(q, w) * q
        n = float(np.linalg.norm(w))
        if n > tol:
            Q_cols.append(w / n)
            keep.append(j)
    if not Q_cols:
        return np.zeros((M.shape[0], 0), dtype=np.float64), keep
    return np.column_stack(Q_cols), keep


def project_report(zero_coords: np.ndarray, cand_coords: np.ndarray, tol: float) -> dict:
    Q, _ = real_orthonormal_columns(cand_coords, tol)
    if zero_coords.shape[1] == 0:
        return {"candidate_rank": int(Q.shape[1]), "zero_dim": 0}
    residual = zero_coords - Q @ (Q.T @ zero_coords) if Q.shape[1] else zero_coords
    residual_norms = np.linalg.norm(residual, axis=0)
    if Q.shape[1]:
        svals = np.linalg.svd(Q.T @ zero_coords, compute_uv=False)
    else:
        svals = np.zeros(0)
    min_cos = float(np.min(svals)) if len(svals) else 0.0
    max_principal_sin = math.sqrt(max(0.0, 1.0 - min(1.0, min_cos) ** 2)) if zero_coords.shape[1] else 0.0
    classified_dim = int(np.sum(svals >= 1.0 - 10 * tol))
    return {
        "candidate_rank": int(Q.shape[1]),
        "zero_dim": int(zero_coords.shape[1]),
        "classified_zero_dimension_by_principal_cosines": classified_dim,
        "unclassified_zero_dimension_by_principal_cosines": int(zero_coords.shape[1] - classified_dim),
        "contains_zero_space_tol": bool(float(np.max(residual_norms)) <= 10 * tol),
        "max_zero_vector_residual_norm": float(np.max(residual_norms)) if len(residual_norms) else 0.0,
        "mean_zero_vector_residual_norm": float(np.mean(residual_norms)) if len(residual_norms) else 0.0,
        "principal_cosines": [float(x) for x in svals],
        "min_principal_cosine": min_cos,
        "max_principal_sine": float(max_principal_sin),
    }


def local_unitary_directions(C0: np.ndarray) -> list[np.ndarray]:
    dirs: list[np.ndarray] = []
    I = np.eye(D, dtype=np.complex128)
    for K in antihermitian_basis(D):
        for Lgen, Rgen in ((np.kron(K, I), None), (np.kron(I, K), None), (None, np.kron(K, I)), (None, np.kron(I, K))):
            if Lgen is not None:
                dirs.append(Lgen @ C0)
            else:
                # derivative of C V^* for V(t)=exp(tK): d/dt C exp(-tK) = - C K
                dirs.append(-C0 @ Rgen)
    return dirs


def explicit_core_family_directions(name: str, C0: np.ndarray) -> list[np.ndarray]:
    """Small named non-orbit equality directions inside the 2x2 singular core.

    These are included for diagnostics. The sphere radial direction is projected
    away later. For both controls, iC0 is phase; for product_projection a
    traceless singular-weight split within the two equal product terms is also
    checked and expected to be negative/not zero after projection.
    """
    dirs = [1j * C0]
    if name == "product_projection":
        # Move weights oppositely between the two support atoms; this is tangent
        # to rank two and unit sphere but is not an equality direction for the
        # gap (it should not be needed to span zero modes).
        E00 = loop8.ketbra(0, 0, 0, 0)
        E10 = loop8.ketbra(1, 0, 1, 0)
        dirs.append((E00 - E10) / math.sqrt(2.0))
    elif name == "diag_difference":
        E00 = loop8.ketbra(0, 0, 0, 0)
        E11 = loop8.ketbra(1, 1, 1, 1)
        dirs.append((E00 + E11) / math.sqrt(2.0))
    return dirs


def candidate_matrix(dirs: Iterable[np.ndarray], C0: np.ndarray, rbasis: list[np.ndarray]) -> np.ndarray:
    cols = []
    for Dv in dirs:
        P = tangent_project(Dv, C0, rbasis)
        c = coords_in_rbasis(P, rbasis)
        if np.linalg.norm(c) > 1e-13:
            cols.append(c)
    if not cols:
        return np.zeros((len(rbasis), 0), dtype=np.float64)
    return np.column_stack(cols)


def analyze_control(name: str, C0: np.ndarray, tol: float) -> dict:
    cbasis = loop8.tangent_complex_basis(C0)
    rbasis = loop8.orthonormalize_real_tangent_sphere(cbasis, C0)
    H = loop8.real_quadratic_matrix(rbasis)
    evals, evecs = np.linalg.eigh(H)
    zero_idx = np.where(np.abs(evals) <= tol)[0]
    zero_coords = evecs[:, zero_idx]

    phase_dirs = [1j * C0]
    local_dirs = local_unitary_directions(C0)
    core_dirs = explicit_core_family_directions(name, C0)

    phase_M = candidate_matrix(phase_dirs, C0, rbasis)
    local_M = candidate_matrix(local_dirs, C0, rbasis)
    core_M = candidate_matrix(core_dirs, C0, rbasis)
    equality_M = np.column_stack([m for m in (phase_M, local_M, core_M) if m.shape[1]])

    reports = {
        "phase_only": project_report(zero_coords, phase_M, tol),
        "local_left_right_product_unitaries": project_report(zero_coords, local_M, tol),
        "phase_plus_local_unitaries": project_report(zero_coords, np.column_stack([phase_M, local_M]), tol),
        "named_core_family_diagnostics": project_report(zero_coords, core_M, tol),
        "all_explicit_equality_candidates": project_report(zero_coords, equality_M, tol),
    }

    # Which candidate columns are actually zero-Hessian directions?
    cand_zero_checks = {}
    for label, M in (("phase", phase_M), ("local", local_M), ("core", core_M)):
        Q, _ = real_orthonormal_columns(M, tol)
        rayleigh = []
        for j in range(Q.shape[1]):
            q = Q[:, j]
            rayleigh.append(float(q @ H @ q))
        cand_zero_checks[label] = {
            "rank": int(Q.shape[1]),
            "rayleigh_min": float(min(rayleigh)) if rayleigh else None,
            "rayleigh_max": float(max(rayleigh)) if rayleigh else None,
            "rayleigh_abs_max": float(max(abs(x) for x in rayleigh)) if rayleigh else None,
        }

    return {
        "name": name,
        "gap": float(loop8.gap(C0)),
        "normalized_gap": float(loop8.normalized_gap(C0)),
        "real_sphere_tangent_dim": len(rbasis),
        "zero_count_tol": int(len(zero_idx)),
        "negative_count_tol": int(np.sum(evals < -tol)),
        "positive_count_tol": int(np.sum(evals > tol)),
        "eigenvalue_min": float(evals[0]),
        "eigenvalue_max": float(evals[-1]),
        "zero_eigenvalues": [float(evals[i]) for i in zero_idx],
        "candidate_reports": reports,
        "candidate_rayleigh_checks": cand_zero_checks,
        "classified_zero_modes_by_explicit_candidates": bool(reports["all_explicit_equality_candidates"]["contains_zero_space_tol"]),
        "classified_dimension_numerical": int(reports["all_explicit_equality_candidates"]["classified_zero_dimension_by_principal_cosines"]),
        "unclassified_dimension_numerical": int(reports["all_explicit_equality_candidates"]["unclassified_zero_dimension_by_principal_cosines"]),
        "caveat": "Local numerical tangent-space containment only; not a global equality classification or proof.",
    }


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--tol", type=float, default=1e-10)
    ap.add_argument("--out", type=Path, default=ROOT / "research_harness/logs/LOOP-0010_zero_mode_symbolic_classification.json")
    args = ap.parse_args()

    controls = loop8.equality_examples()
    res = {
        "loop": "LOOP-0010",
        "claim": "CLAIM-0001-rank-two-partial-trace",
        "tol": args.tol,
        "method": "Compare numerical Hessian zero eigenspace against analytic candidate tangent directions from phase and local product-unitary equality orbits using real Frobenius principal-angle residuals.",
        "controls": {name: analyze_control(name, C0, args.tol) for name, C0 in controls.items()},
        "success_condition_met": False,
        "fail_closed_caveat": "Even complete local containment of zero eigenspaces in explicit candidates is not a global proof of the inequality/equality set.",
    }
    args.out.parent.mkdir(parents=True, exist_ok=True)
    args.out.write_text(json.dumps(res, indent=2) + "\n")

    print(json.dumps({
        name: {
            "zero_count": r["zero_count_tol"],
            "positive_count": r["positive_count_tol"],
            "local_rank": r["candidate_reports"]["local_left_right_product_unitaries"]["candidate_rank"],
            "all_candidate_rank": r["candidate_reports"]["all_explicit_equality_candidates"]["candidate_rank"],
            "all_candidate_contains_zero_space": r["candidate_reports"]["all_explicit_equality_candidates"]["contains_zero_space_tol"],
            "classified_dim": r["candidate_reports"]["all_explicit_equality_candidates"]["classified_zero_dimension_by_principal_cosines"],
            "unclassified_dim": r["candidate_reports"]["all_explicit_equality_candidates"]["unclassified_zero_dimension_by_principal_cosines"],
            "max_residual": r["candidate_reports"]["all_explicit_equality_candidates"]["max_zero_vector_residual_norm"],
            "min_principal_cosine": r["candidate_reports"]["all_explicit_equality_candidates"]["min_principal_cosine"],
        }
        for name, r in res["controls"].items()
    }, indent=2))


if __name__ == "__main__":
    main()
